///////////////////////////////////////////////////////////////////////////////
//
//  Base functor class for analog input.
//
///////////////////////////////////////////////////////////////////////////////

#include "Usul/Functors/Interaction/Input/AnalogInput.h"
#include "Usul/Trace/Trace.h"

using namespace Usul::Functors::Interaction::Input;

USUL_IMPLEMENT_TYPE_ID ( AnalogInput );


///////////////////////////////////////////////////////////////////////////////
//
//  Default constructor.
//
///////////////////////////////////////////////////////////////////////////////

AnalogInput::AnalogInput ( Unknown *caller, const std::string &name ) : 
  BaseClass ( caller, name ),
  _value ( 0 ),
  _range ( -1, 1 )
{
  USUL_TRACE_SCOPE;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

AnalogInput::AnalogInput ( const AnalogInput &a ) : BaseClass ( a ),
  _value ( a._value ),
  _range ( a._range )
{
  USUL_TRACE_SCOPE;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

AnalogInput::~AnalogInput()
{
  USUL_TRACE_SCOPE;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the range.
//
///////////////////////////////////////////////////////////////////////////////

void AnalogInput::range ( const AnalogInput::Vec2 &v )
{
  USUL_TRACE_SCOPE;
  Guard guard ( this->mutex() );
  _range = v;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the range.
//
///////////////////////////////////////////////////////////////////////////////

AnalogInput::Vec2 AnalogInput::range() const
{
  USUL_TRACE_SCOPE;
  Guard guard ( this->mutex() );
  return Vec2 ( _range );
}
