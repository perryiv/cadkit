#include "Usul/Factory/ScopedRegistration.h"
#include "Usul/Factory/ObjectFactory.h"
#include "Usul/Trace/Trace.h"

using namespace Usul::Factory;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

ScopedRegistration::ScopedRegistration ( ObjectFactory &f, BaseCreator *c ) :
  _factory ( f ),
  _name ( ( 0x0 == c ) ? "" : c->name() )
{
  USUL_TRACE_SCOPE;
  _factory.add ( c );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

ScopedRegistration::~ScopedRegistration()
{
  USUL_TRACE_SCOPE;
  _factory.remove ( _name );
}
