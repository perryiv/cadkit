///////////////////////////////////////////////////////////////////////////////
//
//  SlStringIO: Input/output functions for SlString.
//
///////////////////////////////////////////////////////////////////////////////

#ifndef _CADKIT_STANDARD_LIBRARY_STRING_IO_FUNCTIONS_H_
#define _CADKIT_STANDARD_LIBRARY_STRING_IO_FUNCTIONS_H_

#include "SlString.h"

#ifndef _CADKIT_USE_PRECOMPILED_HEADERS
# include <wchar.h>
# include <iostream>
#endif


///////////////////////////////////////////////////////////////////////////////
//
//  Output operator. Keep in global namespace.
//
///////////////////////////////////////////////////////////////////////////////

inline std::ostream &operator << ( std::ostream &out, const CadKit::SlAString &str )
{
  out << str.c_str();
  return out;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Output operator. Keep in global namespace.
//
///////////////////////////////////////////////////////////////////////////////

inline std::wostream &operator << ( std::wostream &out, const CadKit::SlWString &str )
{
  out << str.c_str();
  return out;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Input operator. Keep in global namespace.
//
///////////////////////////////////////////////////////////////////////////////

inline std::istream &operator >> ( std::istream &in, CadKit::SlAString &str )
{
  in >> str.getString();
  return in;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Input operator. Keep in global namespace.
//
///////////////////////////////////////////////////////////////////////////////

inline std::wistream &operator >> ( std::wistream &in, CadKit::SlWString &str )
{
  in >> str.getString();
  return in;
}


#endif // _CADKIT_STANDARD_LIBRARY_STRING_IO_FUNCTIONS_H_
