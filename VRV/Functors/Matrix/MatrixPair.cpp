///////////////////////////////////////////////////////////////////////////////
//
//  Pair of matrix functors.
//
///////////////////////////////////////////////////////////////////////////////

#include "MatrixPair.h"

using namespace VRV::Functors::Matrix;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

MatrixPair::MatrixPair ( 
  Unknown *unknown, 
  MatrixFunctor *c1, 
  MatrixFunctor *c2 ) : 
  BaseClass ( unknown ),
  _c1 ( c1 ),
  _c2 ( c2 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

MatrixPair::MatrixPair ( const MatrixPair &cb ) : BaseClass ( cb ),
  _c1 ( cb._c1 ),
  _c2 ( cb._c2 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

MatrixPair::~MatrixPair()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the matrix.
//
///////////////////////////////////////////////////////////////////////////////

void MatrixPair::operator()()
{
  (*_c1)();
  (*_c2)();
  _matrix = _c1->matrix() * _c2->matrix();
}
