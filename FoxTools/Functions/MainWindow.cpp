///////////////////////////////////////////////////////////////////////////////

#include "FoxTools/Functions/MainWindow.h"

#include <string>
#include <stdexcept>


///////////////////////////////////////////////////////////////////////////////
//
//  Pointer to the main window.
//
///////////////////////////////////////////////////////////////////////////////

namespace FoxTools
{
  namespace Functions
  {
    namespace Detail
    {
      FX::FXMainWindow *_mainWindow ( 0x0 );
    }
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get pointer to the main window. It may be null.
//
///////////////////////////////////////////////////////////////////////////////

FX::FXMainWindow *FoxTools::Functions::mainWindow()
{
  if ( 0x0 == Detail::_mainWindow )
  {
    std::string error ( "Error 1849452464: Accessing null pointer to main window" );
    throw std::runtime_error ( error );
  }

  return Detail::_mainWindow;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set pointer to the main window.
//
///////////////////////////////////////////////////////////////////////////////

void FoxTools::Functions::mainWindow ( FX::FXMainWindow *mw )
{
  Detail::_mainWindow = mw;
}
