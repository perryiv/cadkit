#include "AppFrameWork/Conditions/Or.h"

#include <algorithm>

using namespace AFW::Conditions;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Or::Or() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Or::~Or()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  One of the contained conditions has to be true.
//
///////////////////////////////////////////////////////////////////////////////

bool Or::evaluate()
{
  for ( Group::Iterator i = this->begin(); i != this->end(); ++i )
  {
    if ( true == (*i)->evaluate() )
    {
      return true;
    }
  }
  return false;
}
