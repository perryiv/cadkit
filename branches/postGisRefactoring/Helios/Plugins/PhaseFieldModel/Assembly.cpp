#include "Assembly.h"

USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( Assembly , Assembly::BaseClass );



///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Assembly::Assembly() : 
_sceneElements()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Assembly::~Assembly()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *Assembly::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IAssembly::IID:
    return static_cast < Usul::Interfaces::IAssembly*>(this);
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Return the ith scene element.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::ISceneElement* Assembly::at ( unsigned int i )
{
  return _sceneElements.at( i ).get();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Return the number of scene elements.
//
///////////////////////////////////////////////////////////////////////////////

unsigned int Assembly::size () const
{
  return _sceneElements.size();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Append a scene element.
//
///////////////////////////////////////////////////////////////////////////////

void Assembly::appendSceneElement ( ISceneElement *element )
{
  _sceneElements.push_back ( element );
}
