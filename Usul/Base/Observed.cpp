///////////////////////////////////////////////////////////////////////////////
//
//  Base class that maintains a set of delete-listeners.
//
///////////////////////////////////////////////////////////////////////////////

#include "Usul/Base/Observed.h"

#include "Usul/Adaptors/MemberFunction.h"
#include "Usul/Functions/SafeCall.h"
#include "Usul/Trace/Trace.h"

#include <algorithm>

using namespace Usul::Base;

USUL_IMPLEMENT_TYPE_ID ( Observed );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Observed::Observed() : BaseClass(),
  _deleteListeners()
{
  USUL_TRACE_SCOPE;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

Observed::Observed ( const Observed &m ) : BaseClass ( m )
{
  USUL_TRACE_SCOPE;
  // Do not copy the delete-listeners; it doesn't make sense.
}


///////////////////////////////////////////////////////////////////////////////
//
//  Assignment.
//
///////////////////////////////////////////////////////////////////////////////

Observed &Observed::operator = ( const Observed &m )
{
  USUL_TRACE_SCOPE;
  // Do not assign the delete-listeners; it doesn't make sense.
  return *this;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Observed::~Observed()
{
  USUL_TRACE_SCOPE;
  Usul::Functions::safeCall ( Usul::Adaptors::memberFunction ( this, &Observed::_destroy ), "3168745373" );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destroy the object.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::_destroy()
{
  USUL_TRACE_SCOPE;
  Guard guard ( this );
  _deleteListeners.clear();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Notify the delete-listeners.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::_notifyDeleteListeners()
{
  USUL_TRACE_SCOPE;

  // Make a copy of the listeners.
  DeleteListeners listeners;
  {
    Guard guard ( this );
    listeners = _deleteListeners;
  }

  // Notify all the listeners.
  std::for_each ( listeners.begin(), listeners.end(), 
    std::bind2nd ( std::mem_fun ( &IDeleteListener::objectDeleted ), this ) );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query the interfaces
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *Observed::queryInterface ( unsigned long iid )
{
  USUL_TRACE_SCOPE;
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IDeleteNotify::IID:
    return static_cast < Usul::Interfaces::IDeleteNotify* > ( this );
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Reference this object.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::ref()
{
  USUL_TRACE_SCOPE;
  BaseClass::ref();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Dereference this object.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::unref ( bool allowDeletion )
{
  USUL_TRACE_SCOPE;

  // If we're going to be deleted then notify the listeners.
  if ( 1 == this->refCount() )
  {
    this->_notifyDeleteListeners();
  }

  // Call base class.
  BaseClass::unref ( allowDeletion );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add the listener.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::addDeleteListener ( Usul::Interfaces::IUnknown *unknown )
{
  USUL_TRACE_SCOPE;
  Guard guard ( this );

  IDeleteListener::QueryPtr listener ( unknown );
  if ( true == listener.valid() )
  {
    _deleteListeners.insert ( listener );
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Remove the listener.
//
///////////////////////////////////////////////////////////////////////////////

void Observed::removeDeleteListener ( Usul::Interfaces::IUnknown *unknown )
{
  USUL_TRACE_SCOPE;
  Guard guard ( this );

  IDeleteListener::QueryPtr listener ( unknown );
  if ( true == listener.valid() )
  {
    _deleteListeners.erase ( listener );
  }
}
