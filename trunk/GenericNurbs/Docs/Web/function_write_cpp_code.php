
<?php

///////////////////////////////////////////////////////////////////////////////
//
//  Write C++ code.
//
///////////////////////////////////////////////////////////////////////////////

function write_cpp ( $text )
{
  echo "<font family=\"courior\" face=\"Courier New\" size=-1>$text</font>";
}


///////////////////////////////////////////////////////////////////////////////
//
//  Write C++ code as a link.
//
///////////////////////////////////////////////////////////////////////////////

function write_cpp_as_external_link ( $text, $url )
{
  $link = "<a href=\"$url\" target=\"blank\">$text</a>";
  write_cpp ( $link );
}

