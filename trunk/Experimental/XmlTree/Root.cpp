///////////////////////////////////////////////////////////////////////////////
//
//  XML tree root class.
//
///////////////////////////////////////////////////////////////////////////////

#include "XmlTree/Root.h"
#include "XmlTree/Detail/RootImpl.h"

using namespace XmlTree;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Root::Root() : BaseClass(),
  _root ( new XmlTree::Detail::RootImpl )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Root::Root ( const std::string &file ) : BaseClass(),
  _root ( new XmlTree::Detail::RootImpl )
{
  this->load ( file );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor
//
///////////////////////////////////////////////////////////////////////////////

Root::~Root()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Load the file.
//
///////////////////////////////////////////////////////////////////////////////

void Root::load ( const std::string &file )
{
  _root->load ( file );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Clear the tree.
//
///////////////////////////////////////////////////////////////////////////////

void Root::clear()
{
  _root->clear();
}
