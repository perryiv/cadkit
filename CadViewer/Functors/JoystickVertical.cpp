///////////////////////////////////////////////////////////////////////////////
//
//  Functor class for vertical joystick input.
//
///////////////////////////////////////////////////////////////////////////////

#include "Precompiled.h"
#include "JoystickVertical.h"

using namespace CV;
using namespace CV::Functors;


///////////////////////////////////////////////////////////////////////////////
//
//  Default constructor.
//
///////////////////////////////////////////////////////////////////////////////

JoystickVertical::JoystickVertical ( Unknown *u ) : BaseClass ( u ),
  _j ( u )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

JoystickVertical::JoystickVertical ( const JoystickVertical &j ) : BaseClass ( j ),
  _j ( j._j )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

JoystickVertical::~JoystickVertical()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the vertical analog input.
//
///////////////////////////////////////////////////////////////////////////////

void JoystickVertical::operator()()
{
  _value = _j->joystickVertical();
}
