///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Text::Text() : 
  BaseClass(), 
  _text(),
  _alignment ( LEFT ),
  _wrapLine ( false ),
  _fontSize ( 25.0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Text::Text( const std::string& text ) : 
  BaseClass(), 
  _text( text ),
  _alignment ( LEFT ),
  _wrapLine ( false ),
  _fontSize ( 25.0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Text::~Text()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the text.
//
///////////////////////////////////////////////////////////////////////////////

void Text::text( const std::string& t )
{
  _text = t;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the text.
//
///////////////////////////////////////////////////////////////////////////////

const std::string& Text::text() const
{
  return _text;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Build the scene.
//
///////////////////////////////////////////////////////////////////////////////

osg::Node* Text::buildScene()
{
  const SizeType width  ( this->size()[0] );
  SizeType height ( this->size()[1] );
  
  osg::ref_ptr< osg::Geode > geode ( new osg::Geode );

  osg::ref_ptr < osgText::Text > text ( new osgText::Text );

  osg::ref_ptr<osgText::Font> font ( OsgTools::Font::defaultFont() );
  text->setFont( font.get() );

  if ( LEFT == _alignment )
    text->setPosition ( osg::Vec3( 0.0, height / 4, 0.0 ) );
  else if ( RIGHT == _alignment )
  {
    text->setPosition ( osg::Vec3 ( width, height / 4, 0.0 ) );
    text->setAlignment ( osgText::Text::RIGHT_BASE_LINE );
  }

  //text->setAutoRotateToScreen( true );
  //text->setCharacterSizeMode( osgText::Text::SCREEN_COORDS );
  text->setCharacterSizeMode( osgText::Text::OBJECT_COORDS );
  text->setCharacterSize( this->fontSize() );
  text->setText ( this->text() );

  if ( this->wrapLine() )
  {
    text->setPosition ( osg::Vec3( 0.0, height - ( this->fontSize() / 2.0 ), 0.0 ) );
    text->setMaximumWidth ( width );
  }
  else
  {
    // Resize the fit on the line.
    while ( ( OsgTools::Font::estimateTextWidth( text.get() ) * .75 ) > width )
    {
      height -= 2;
      text->setCharacterSize ( height );
    }
  }

  geode->addDrawable( text.get() );
  
  osg::ref_ptr<osg::StateSet> ss ( geode->getOrCreateStateSet() );
  //ss->setRenderBinDetails ( 11, "RenderBin" );
  ss->setMode( GL_DEPTH_TEST, osg::StateAttribute::OFF | osg::StateAttribute::PROTECTED );

  // Turn off lighting.
  OsgTools::State::StateSet::setLighting ( geode.get(), false );

  return geode.release();
}


