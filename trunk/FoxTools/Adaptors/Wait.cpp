///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Wait::Wait ( FX::FXApp *app ) : _app ( app )
{
  if ( _app )
    _app->beginWaitCursor();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Wait::~Wait()
{
  if ( _app )
    _app->endWaitCursor();
}
