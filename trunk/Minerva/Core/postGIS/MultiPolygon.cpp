#include "MultiPolygon.h"

using namespace Minerva::Core::postGIS;

///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiPolygon::MultiPolygon() : BaseClass(),
_polygons()
{
}

///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiPolygon::~MultiPolygon()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a polygon.
//
///////////////////////////////////////////////////////////////////////////////

void MultiPolygon::addPolygon  ( Polygon* polygon )
{
  if( 0x0 != polygon )
    _polygons.push_back ( polygon );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a geometry.  Make sure it's a polygon before it's added
//
///////////////////////////////////////////////////////////////////////////////

void MultiPolygon::addGeometry ( Geometry* geometry )
{
  if( Polygon *polygon = dynamic_cast < Polygon* > ( geometry ) )
    this->addPolygon ( polygon );
}
