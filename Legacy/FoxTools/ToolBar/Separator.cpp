#include "FoxTools/ToolBar/Separator.h"
#include "FoxTools/Headers/Separator.h"

using namespace FoxTools::ToolBar;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor
//
///////////////////////////////////////////////////////////////////////////////

Separator::Separator( unsigned int token ) : 
  BaseClass ( token ),
  _separator ( 0x0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor
//
///////////////////////////////////////////////////////////////////////////////

Separator::~Separator()
{
  if( _separator )
    delete _separator;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Build
//
///////////////////////////////////////////////////////////////////////////////

void Separator::_build ( FX::FXComposite *parent )
{
  if ( !_separator )
  {
    _separator = new FX::FXSeparator ( parent, FX::SEPARATOR_GROOVE );
    _separator->setTarget   ( this->target()   );
    _separator->setSelector ( this->selector() );
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Clear
//
///////////////////////////////////////////////////////////////////////////////

void Separator::clear()
{
  if( _separator )
    delete _separator;
  _separator = 0x0;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Create.
//
///////////////////////////////////////////////////////////////////////////////

void Separator::_create ()
{
  // Create the seperator if we can.
  if ( _separator && !_separator->id() )
    _separator->create();
}

