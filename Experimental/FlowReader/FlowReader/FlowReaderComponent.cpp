#include "FlowReaderComponent.h"
#include "FlowReaderDocument.h"

#include "Usul/Components/Factory.h"

#include <algorithm>

USUL_DECLARE_COMPONENT_FACTORY ( FlowReaderComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( FlowReaderComponent, FlowReaderComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

FlowReaderComponent::FlowReaderComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

FlowReaderComponent::~FlowReaderComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *FlowReaderComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin * > ( this );
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate * > ( this );
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *FlowReaderComponent::createDocument ( Unknown *caller )
{
  FlowReaderDocument::ValidRefPtr document ( new FlowReaderDocument );
  return document.release();
}

