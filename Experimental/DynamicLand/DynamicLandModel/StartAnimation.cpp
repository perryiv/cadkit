void StartAnimation::_execute ()
{
  USUL_TRACE_SCOPE;
  //this->_dummy();
#if 1

  Usul::Interfaces::IDldNavigator::QueryPtr nav ( this->caller() );
  
  if ( nav.valid () )
  {
    nav->animate( true );
  }
#endif
}

