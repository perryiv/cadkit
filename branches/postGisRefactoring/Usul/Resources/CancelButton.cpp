//  Get and set pointer to the cancel button.
//
///////////////////////////////////////////////////////////////////////////////

#include "Usul/Resources/CancelButton.h"
#include "Usul/Interfaces/GUI/ICancelButton.h"

///////////////////////////////////////////////////////////////////////////////
//
//  Pointer to the cancel button.
//
///////////////////////////////////////////////////////////////////////////////

namespace Usul
{
  namespace Resources
  {
    namespace Detail
    {
      Usul::Interfaces::ICancelButton::QueryPtr _cancelButton;
    }
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get pointer to the cancel button. It may be null.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::ICancelButton *Usul::Resources::cancelButton()
{
  return Detail::_cancelButton.get();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set pointer to the cancel button.
//
///////////////////////////////////////////////////////////////////////////////

void Usul::Resources::cancelButton ( Usul::Interfaces::ICancelButton *cancelButton )
{
  Detail::_cancelButton = cancelButton;
} 
