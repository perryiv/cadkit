#include "AppFrameWork/Dialogs/Dialog.h"
#include "AppFrameWork/Core/BaseVisitor.h"

using namespace AFW::Dialogs;

AFW_IMPLEMENT_OBJECT ( Dialog );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Dialog::Dialog() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Dialog::~Dialog()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Accept the visitor.
//
///////////////////////////////////////////////////////////////////////////////

void Dialog::accept ( AFW::Core::BaseVisitor *v )
{
  Guard guard ( this->mutex() );
  if ( v )
    v->visit ( this );
}
