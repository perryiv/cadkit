#include "GSG/Core/Precompiled.h"
#include "GSG/Core/Draw.h"

using namespace GSG;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Draw::Draw ( RenderBin *bin ) : BinBuilder ( bin ),
  _background()
{
  // Empty.
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

Draw::Draw ( const Draw &r ) : BinBuilder ( r ),
  _background ( r._background )
{
  // Empty.
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Draw::~Draw()
{
  // Empty.
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the background color.
//
///////////////////////////////////////////////////////////////////////////////

void Draw::backgroundColor ( const Color &background )
{
  Lock lock ( this );
  _background = background;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the background color.
//
///////////////////////////////////////////////////////////////////////////////

const Color &Draw::backgroundColor() const
{
  Lock lock ( this );
  return _background;
}
