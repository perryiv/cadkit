///////////////////////////////////////////////////////////////////////////////
//
//  Enable or disable the window.
//
///////////////////////////////////////////////////////////////////////////////

void FoxTools::Functions::enable ( bool state, FX::FXObject *object )
{
  FX::FXWindow *window = SAFE_CAST_FOX ( FX::FXWindow, object );
  if ( window )
  {
    if ( state )
      window->enable();
    else
      window->disable();
  }
}
