namespace Detail
{
  struct Init
  {
    Init()
    {
      GDALAllRegister();
      OGRRegisterAll();
    }
    ~Init()
    {
      GDALDestroyDriverManager();
      OGRCleanupAll();
    }
  } _init;
}
