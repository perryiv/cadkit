#include "Minerva/Core/Animate/Settings.h"

using namespace Minerva::Core::Animate;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Settings::Settings() : 
  BaseClass(),
  _firstDate( boost::date_time::min_date_time ),
  _lastDate( boost::date_time::max_date_time ),
  _animate( false ),
  _pause ( false ),
  _showPastDays( false ),
  _timeWindow ( false ),
  _windowLength ( 0 ),
  _type ( Settings::TIMESTEP_TYPE_DAY )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Settings::~Settings()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the min date.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::firstDate( const Date& date )
{
  _firstDate = date;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the min date.
//
///////////////////////////////////////////////////////////////////////////////

const Minerva::Core::Animate::Date& Settings::firstDate() const
{
  return _firstDate;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the max date.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::lastDate( const Date& date )
{
  _lastDate = date;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the max date.
//
///////////////////////////////////////////////////////////////////////////////

const Minerva::Core::Animate::Date& Settings::lastDate() const
{
  return _lastDate;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set show past days flag.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::showPastDays( bool b )
{
  _showPastDays = b;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Should past days be shown?
//
///////////////////////////////////////////////////////////////////////////////

bool Settings::showPastDays() const
{
  return _showPastDays;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the time window flag.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::timeWindow( bool b )
{
  _timeWindow = b;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Should there be a time window?
//
///////////////////////////////////////////////////////////////////////////////

bool Settings::timeWindow() const
{
  return _timeWindow;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the time window length.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::windowLength ( unsigned int length )
{
  _windowLength = length;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the time window length.
//
///////////////////////////////////////////////////////////////////////////////

unsigned int Settings::windowLength () const
{
  return _windowLength;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the animate flag.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::animate( bool b )
{
  _animate = b;

  this->pause ( false );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the animate flag.
//
///////////////////////////////////////////////////////////////////////////////

bool Settings::animate() const
{
  return _animate;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the timestep type.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::timestepType( TimestepType type )
{
  _type = type;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the timestep type.
//
///////////////////////////////////////////////////////////////////////////////

Settings::TimestepType Settings::timestepType() const
{
  return _type;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the pause flag.
//
///////////////////////////////////////////////////////////////////////////////

void Settings::pause ( bool b )
{
  _pause = b;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the pause flag.
//
///////////////////////////////////////////////////////////////////////////////

bool Settings::pause () const
{
  return _pause;
}

