#include "VoxelComponent.h"
#include "VoxelDocument.h"

USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( VoxelComponent , VoxelComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

VoxelComponent::VoxelComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

VoxelComponent::~VoxelComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *VoxelComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate*>(this);
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin*>(this);
  case Usul::Interfaces::INewDocumentCreate::IID:
    return static_cast < Usul::Interfaces::INewDocumentCreate* > ( this );
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *VoxelComponent::createDocument ( Unknown *caller )
{
  VoxelDocument::ValidRefPtr document ( new VoxelDocument );
  return document.release();
}


/////////////////////////////////////////////////////////////////////////////
//
//  Get name of Document that is created
//
/////////////////////////////////////////////////////////////////////////////

std::string VoxelComponent::documentTypeName()
{
  VoxelDocument::ValidRefPtr document ( new VoxelDocument );
  return document->typeName();
}
