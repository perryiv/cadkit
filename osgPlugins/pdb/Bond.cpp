#include "Bond.h"
#include "Atom.h"

#include <sstream>

Bond::Bond(const Atom& lhs, const Atom& rhs, int id)
{
  _id = id;
  _point1 = lhs.getVec3();
  _point2 = rhs.getVec3();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Bond::~Bond()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Returns data members as a string.
//
///////////////////////////////////////////////////////////////////////////////

const std::string Bond::toString() const
{
  std::ostringstream out;
	out << _id << " " << _point1[0] << " " <<  _point1[1] << " " <<  _point1[2];
	return std::string(out.str());
}
