#include "Brahma/Scene/Transform.h"
#include "Brahma/Scene/NodeVisitor.h"

///////////////////////////////////////////////////////////////////////////////
//
// Get type of this transform node.
//
///////////////////////////////////////////////////////////////////////////////

const std::string& Brahma::Scene::Transform::type() const 
{
  return _type;
}


///////////////////////////////////////////////////////////////////////////////
//
// Set type for this transform node. 
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::Transform::type( const std::string& type )
{
  _type = type;
}


///////////////////////////////////////////////////////////////////////////////
//
// Get position. 
//
///////////////////////////////////////////////////////////////////////////////

const Usul::Math::Vec3f& Brahma::Scene::Transform::position() const
{
  return _position;
}


///////////////////////////////////////////////////////////////////////////////
//
// Set position. 
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::Transform::position( const Usul::Math::Vec3f& position )
{
  _position = position;
}


///////////////////////////////////////////////////////////////////////////////
//
// Get rotation.
//
///////////////////////////////////////////////////////////////////////////////

const Usul::Math::Vec4f& Brahma::Scene::Transform::rotation() const
{
  return _rotation;
}


///////////////////////////////////////////////////////////////////////////////
//
// Set rotation.
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::Transform::rotation( const Usul::Math::Vec4f& rotation )
{
  _rotation = rotation;
}


///////////////////////////////////////////////////////////////////////////////
//
// Get pivot point about which rotation will be applied. 
// Default is Object space center. 
//
///////////////////////////////////////////////////////////////////////////////

const Usul::Math::Vec3f& Brahma::Scene::Transform::pivot() const
{
  return _pivot;
}


///////////////////////////////////////////////////////////////////////////////
//
// Set pivot point about which rotation will be applied. 
// Default is Object space center. 
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::Transform::pivot( const Usul::Math::Vec3f& pivot )
{
  _pivot = pivot;
}


///////////////////////////////////////////////////////////////////////////////
//
// Accept the node visitor. 
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::Transform::accept( NodeVisitor& nv )
{
	nv.visit( *this );
}
