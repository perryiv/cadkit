#include "Brahma/Scene/LeafNode.h"


///////////////////////////////////////////////////////////////////////////////
//
// Constructor. 
//
///////////////////////////////////////////////////////////////////////////////

Brahma::Scene::LeafNode::LeafNode() : BaseClass(),
  _data ( "" )
{
  this->_addMember( "_data", _data );
}


///////////////////////////////////////////////////////////////////////////////
//
// Destructor. 
//
///////////////////////////////////////////////////////////////////////////////

Brahma::Scene::LeafNode::~LeafNode()
{
}


///////////////////////////////////////////////////////////////////////////////
//
// Get file/dir path.
//
///////////////////////////////////////////////////////////////////////////////

const std::string& Brahma::Scene::LeafNode::data() const
{
	return _data;
}


///////////////////////////////////////////////////////////////////////////////
//
// Set file/dir path.
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::LeafNode::data( const std::string& source )
{
  _data = source;
}


///////////////////////////////////////////////////////////////////////////////
//
// Accept node visitor. 
//
///////////////////////////////////////////////////////////////////////////////

void Brahma::Scene::LeafNode::accept( Brahma::Scene::NodeVisitor &nv )
{
	nv.visit( *this );
}
