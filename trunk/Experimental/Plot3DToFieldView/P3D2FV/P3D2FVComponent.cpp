#include "P3D2FVComponent.h"
#include "P3D2FVDocument.h"

#include "Usul/Components/Factory.h"

#include <algorithm>

USUL_DECLARE_COMPONENT_FACTORY ( P3D2FVComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( P3D2FVComponent, P3D2FVComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

P3D2FVComponent::P3D2FVComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

P3D2FVComponent::~P3D2FVComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *P3D2FVComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin * > ( this );
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate * > ( this );
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *P3D2FVComponent::createDocument ( Unknown *caller )
{
  P3D2FVDocument::ValidRefPtr document ( new P3D2FVDocument );
  return document.release();
}

