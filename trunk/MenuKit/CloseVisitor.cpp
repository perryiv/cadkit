///////////////////////////////////////////////////////////////////////////////
//
//  Visitor that closes the menu.
//
///////////////////////////////////////////////////////////////////////////////

#include "CloseVisitor.h"
#include "Menu.h"

using namespace MenuKit;


///////////////////////////////////////////////////////////////////////////////
//
//  Default constructor.
//
///////////////////////////////////////////////////////////////////////////////

CloseVisitor::CloseVisitor(): BaseClass ( BaseClass::ALL )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

CloseVisitor::~CloseVisitor()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Apply the visitor to the menu.
//
///////////////////////////////////////////////////////////////////////////////

void CloseVisitor::apply ( Menu &m )
{
  m.expanded ( false );
  this->traverse ( m );
}
