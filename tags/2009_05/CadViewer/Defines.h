#ifndef _CV_DEFINES_H_
#define _CV_DEFINES_H_

#include <sstream>


///////////////////////////////////////////////////////////////////////////////
//
//  Helper function to format a string holding the location.
//
///////////////////////////////////////////////////////////////////////////////

namespace CV 
{
  struct _Helper
  {
    static std::string _format ( const char *file, unsigned int line )
    {
      std::ostringstream message;
      message << "\nFile: " << file << '\n';
      message << "Line: " << line;
      return message.str();
    }
  }; // struct _Helper
}; // namespace CV


///////////////////////////////////////////////////////////////////////////////
//
//  Macro to format a string holding the location.
//
///////////////////////////////////////////////////////////////////////////////

#ifdef _WIN32
# define CV_LOCATION std::string ( __FILE__ )
#else
# define CV_LOCATION CV::_Helper::_format ( __FILE__, __LINE__ )
#endif


#endif // _CV_DEFINES_H_
