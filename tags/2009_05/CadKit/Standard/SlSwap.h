//
//  SlSwap: Inline functions for swapping values.
//
///////////////////////////////////////////////////////////////////////////////

#ifndef _CADKIT_STANDARD_LIBRARY_INLINE_SWAP_FUNCTIONS_H_
#define _CADKIT_STANDARD_LIBRARY_INLINE_SWAP_FUNCTIONS_H_


namespace CadKit
{
///////////////////////////////////////////////////////////////////////////////
//
//  Swap the values of a and b.
//
///////////////////////////////////////////////////////////////////////////////

template<class T> inline void swap_impl ( T &a, T &b, T &temp )
{
  temp = a;
  a = b;
  b = temp;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Swap the values of a and b.
//
///////////////////////////////////////////////////////////////////////////////

template<class T> inline void swap_impl ( T &a, T &b )
{
  T temp ( a );
  a = b;
  b = temp;
}


}; // namespace CadKit.


///////////////////////////////////////////////////////////////////////////////
//
// For convenience and backward compatability.
//
///////////////////////////////////////////////////////////////////////////////

#define SL_SWAP   CadKit::swap_impl


#endif // _CADKIT_STANDARD_LIBRARY_INLINE_SWAP_FUNCTIONS_H_
