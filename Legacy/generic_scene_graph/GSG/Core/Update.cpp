Update::~Update()
{
  // Empty.
}


///////////////////////////////////////////////////////////////////////////////
//
//  The visit functions.
//
///////////////////////////////////////////////////////////////////////////////

GSG_DEFINE_DELEGATING_VISIT_NODE_FUNCTIONS ( Update, _update );


///////////////////////////////////////////////////////////////////////////////
//
//  Update the node.
//
///////////////////////////////////////////////////////////////////////////////

void Update::_update ( Node &n )
{
  Lock lock ( this );
  n.update();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Update the group.
//
///////////////////////////////////////////////////////////////////////////////

void Update::_update ( Group &g )
{
  Lock lock ( this );
  this->_traverse ( g ); // Traverse first.
  this->_update ( static_cast < Node & > ( g ) );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Set from the given object.
//
/////////////////////////////////////////////////////////////////////////////

void Update::setFrom ( const Update &u )
{
  // Call the base class's function.
  BaseClass::setFrom ( u );
}
