#ifndef _FOX_TOOLS_CREATE_FUNCTIONS_H_
#define _FOX_TOOLS_CREATE_FUNCTIONS_H_

#include "FoxTools/Headers/Window.h"


namespace FoxTools {
namespace Functions {


///////////////////////////////////////////////////////////////////////////////
//
//  Create if we should.
//
///////////////////////////////////////////////////////////////////////////////

void create ( FX::FXWindow *window )
{
  if ( window )
  {
    if ( window->getParent() )
    {
      if ( window->getParent()->id() )
      {
        window->create();
      }
    }
  }
}


}; // namespace Functions
}; // namespace FoxTools


#endif // _FOX_TOOLS_CHILD_FUNCTIONS_H_
