{
  USUL_TRACE_SCOPE;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Render the scene.
//
///////////////////////////////////////////////////////////////////////////////

void Group::_render ( osg::Node *node )
{
  USUL_TRACE_SCOPE;

  // Loop through all the renderers.
  Renderers renderers ( Usul::Threads::Safe::get ( this->mutex(), _renderers ) );
  for ( Renderers::iterator i = renderers.begin(); i != renderers.end(); ++i )
  {
    Renderer::RefPtr r ( *i );
    if ( true == r.valid() )
    {
      // Render the scene.
      r->render ( node );
    }
  }
}
