///////////////////////////////////////////////////////////////////////////////
//
//  Get and set pointer to the menu bar.
//
///////////////////////////////////////////////////////////////////////////////

#include "Usul/Resources/MenuBar.h"
#include "Usul/Interfaces/Fox/IMenuBar.h"

///////////////////////////////////////////////////////////////////////////////
//
//  Pointer to the cancel button.
//
///////////////////////////////////////////////////////////////////////////////

namespace Usul
{
  namespace Resources
  {
    namespace Detail
    {
      Usul::Interfaces::IMenuBar::QueryPtr _menuBar;
    }
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get pointer to the menu bar. It may be null.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IMenuBar *Usul::Resources::menuBar()
{
  return Detail::_menuBar.get();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set pointer to the menu bar.
//
///////////////////////////////////////////////////////////////////////////////

void Usul::Resources::menuBar ( Usul::Interfaces::IMenuBar *menuBar )
{
  Detail::_menuBar = menuBar;
} 
