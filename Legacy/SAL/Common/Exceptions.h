#ifndef _SAL_EXCEPTION_CLASSES_H_
#define _SAL_EXCEPTION_CLASSES_H_

#include <stdexcept>


namespace SAL {
namespace Exceptions {


///////////////////////////////////////////////////////////////////////////////
//
//  Base class for exceptions.
//
///////////////////////////////////////////////////////////////////////////////

struct Exception : public std::runtime_error
{
  Exception ( const std::string &what ) : std::runtime_error ( what ){}
};


///////////////////////////////////////////////////////////////////////////////
//
//  Exception for file not read.
//
///////////////////////////////////////////////////////////////////////////////

struct FailedToRead : public Exception
{
  FailedToRead ( const std::string &what ) : Exception ( what ){}
};


///////////////////////////////////////////////////////////////////////////////
//
//  Exception for unknown node type.
//
///////////////////////////////////////////////////////////////////////////////

struct UnknownNodeType : public Exception
{
  UnknownNodeType ( const std::string &what ) : Exception ( what ){}
};


///////////////////////////////////////////////////////////////////////////////
//
//  Exception for file not written.
//
///////////////////////////////////////////////////////////////////////////////

struct FailedToWrite : public Exception
{
  FailedToWrite ( const std::string &what ) : Exception ( what ){}
};


///////////////////////////////////////////////////////////////////////////////
//
//  Exception for an out-of-range index.
//
///////////////////////////////////////////////////////////////////////////////

struct IndexOutOfRange : public Exception
{
  IndexOutOfRange ( const std::string &what ) : Exception ( what ){}
};


}; // namespace Exceptions
}; // namespace SAL


#endif // _SAL_EXCEPTION_CLASSES_H_
