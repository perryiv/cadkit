///////////////////////////////////////////////////////////////////////////////
//
//  XML document class.
//
///////////////////////////////////////////////////////////////////////////////

#include "XmlTree/Document.h"
#include "XmlTree/Loader.h"
#include "XmlTree/Writer.h"

using namespace XmlTree;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Document::Document() : BaseClass ( "document" ),
  _header ( XmlTree::Constants::HEADER )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Document::Document ( const std::string &file ) : BaseClass ( "document" ),
  _header ( XmlTree::Constants::HEADER )
{
  this->load ( file );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor
//
///////////////////////////////////////////////////////////////////////////////

Document::~Document()
{
  this->clear();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Load the file.
//
///////////////////////////////////////////////////////////////////////////////

void Document::load ( const std::string &file )
{
  Loader loader;
  loader.load ( file, this );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Write to file.
//
///////////////////////////////////////////////////////////////////////////////

void Document::write ( const std::string &file )
{
  Writer writer;
  writer.write ( this, file );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Write to file.
//
///////////////////////////////////////////////////////////////////////////////

void Document::write ( std::ostream &out )
{
  Writer writer;
  writer.write ( this, out );
}
