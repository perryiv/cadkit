#include "Pair.h"

using namespace VRV::Functors;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Pair::Pair ( 
  Unknown *unknown, 
  BaseFunctor *c1, 
  BaseFunctor *c2,
  unsigned int id ) : 
  BaseClass ( unknown, id ),
  _c1 ( c1 ),
  _c2 ( c2 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

Pair::Pair ( const Pair &cb ) : BaseClass ( cb ),
  _c1 ( cb._c1 ),
  _c2 ( cb._c2 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Pair::~Pair()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Have the transforms do their thing.
//
///////////////////////////////////////////////////////////////////////////////

void Pair::operator()()
{
  if ( _c1.valid() )
    (*_c1)();
  if ( _c2.valid() )
    (*_c2)();
}
