#include "ParadisModelComponent.h"
#include "ParadisDocument.h"

USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( ParadisModelComponent , ParadisModelComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
// Constructor
//
///////////////////////////////////////////////////////////////////////////////

ParadisModelComponent::ParadisModelComponent() 
{
}


///////////////////////////////////////////////////////////////////////////////
//
// Destructor
//
///////////////////////////////////////////////////////////////////////////////

ParadisModelComponent::~ParadisModelComponent() 
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *ParadisModelComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate*>(this);
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin* > ( this );
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
// Usul/Interfaces/IPlugin.h implementation
//
///////////////////////////////////////////////////////////////////////////////

std::string ParadisModelComponent::getPluginName() const 
{
  return "Paradis Model";
}


///////////////////////////////////////////////////////////////////////////////
//
//  Create the document.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document * ParadisModelComponent::createDocument ( Usul::Interfaces::IUnknown *caller )
{
  ParadisDocument::ValidRefPtr doc ( new ParadisDocument );
  return doc.release();
}
