#ifndef _USUL_MATH_ABSOLUTE_VALUE_H_
#define _USUL_MATH_ABSOLUTE_VALUE_H_


namespace Usul {
namespace Math {


/////////////////////////////////////////////////////////////////////////////
//
//  Return the absolute value.
//
/////////////////////////////////////////////////////////////////////////////

template < class T > inline T absolute ( T v )
{
  return ( v < 0 ) ? -v : v;
}


} // namespace Math
} // namespace Usul


#endif // _USUL_MATH_ABSOLUTE_VALUE_H_
