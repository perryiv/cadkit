#include "MultiPoint.h"

using namespace Minerva::Core::postGIS;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiPoint::MultiPoint() : BaseClass(),
_points()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiPoint::~MultiPoint()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a point.
//
///////////////////////////////////////////////////////////////////////////////

void MultiPoint::addPoint( Point* point )
{
  if( 0x0 != point )
    _points.push_back( point );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a geometry.  Make sure it's a point before adding.
//
///////////////////////////////////////////////////////////////////////////////

void MultiPoint::addGeometry ( Geometry *geometry )
{
  if( Point* point = dynamic_cast < Point * > ( geometry ) )
    this->addPoint ( point );
}
