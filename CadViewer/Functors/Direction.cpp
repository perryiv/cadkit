///////////////////////////////////////////////////////////////////////////////
//
//  Functor for getting a direction.
//
///////////////////////////////////////////////////////////////////////////////

#include "Precompiled.h"
#include "Direction.h"

using namespace CV;
using namespace CV::Functors;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Direction::Direction ( Unknown *unknown, const Vector &dir, MatrixFunctor *mf ) : 
  BaseClass ( unknown, 1076045327 ),
  _dir ( dir ),
  _original ( dir ),
  _mf ( mf )
{
  // Make sure the vectors are normalized.
  _dir.normalize();
  _original.normalize();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

Direction::Direction ( const Direction &d ) : 
  BaseClass ( d ),
  _dir ( d._dir ),
  _original ( d._original ),
  _mf ( d._mf )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Direction::~Direction()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the direction.
//
///////////////////////////////////////////////////////////////////////////////

void Direction::operator()()
{
  // If we have a valid matrix-functor...
  if ( _mf.valid() )
  {
    // Update and get the matrix.
    (*_mf)();
    const MatrixFunctor::Matrix& M = _mf->matrix();

    // Transform the direction vector and normalize.
    _dir = M * _original;
    _dir.normalize();
  }
}
