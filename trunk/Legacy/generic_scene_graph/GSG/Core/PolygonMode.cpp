#include "GSG/Core/Precompiled.h"
#include "GSG/Core/PolygonMode.h"

using namespace GSG;

GSG_IMPLEMENT_REFERENCED ( PolygonMode );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

PolygonMode::PolygonMode ( Mode m ) : Attribute(),
  _mode ( m )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

PolygonMode::PolygonMode ( const PolygonMode &m ) : Attribute ( m ),
  _mode ( m._mode )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

PolygonMode::~PolygonMode()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the mode.
//
///////////////////////////////////////////////////////////////////////////////

void PolygonMode::mode ( Mode m )
{
  Lock lock ( this );
  _mode = m;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Assignment.
//
///////////////////////////////////////////////////////////////////////////////

PolygonMode &PolygonMode::operator = ( const PolygonMode &rhs )
{
  this->mode ( rhs._mode );
  return *this;
}


/////////////////////////////////////////////////////////////////////////////
//
//  Set from the given object.
//
/////////////////////////////////////////////////////////////////////////////

void PolygonMode::setFrom ( const PolygonMode &m )
{
  Lock lock ( this );

  // Set the members.
  _mode = m._mode;

  // Call the base class's function.
  BaseClass::setFrom ( m );
}
