///////////////////////////////////////////////////////////////////////////////
//
//  Class to represent point data plus a date.
//
///////////////////////////////////////////////////////////////////////////////

#include "Minerva/Core/DataObjects/PointTime.h"

using namespace Minerva::Core::DataObjects;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

PointTime::PointTime( const std::string& first, const std::string& last ) : 
_firstDate ( first ),
_lastDate ( last )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

PointTime::~PointTime()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the first date.
//
///////////////////////////////////////////////////////////////////////////////

const OsgTools::Animate::Date& PointTime::firstDate() const
{
  return _firstDate;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the last date.
//
///////////////////////////////////////////////////////////////////////////////

const OsgTools::Animate::Date& PointTime::lastDate() const
{
  return _lastDate;
}
