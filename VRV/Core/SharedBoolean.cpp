#include "SharedBoolean.h"

#include "vpr/IO/ObjectReader.h"
#include "vpr/IO/ObjectWriter.h"

using namespace VRV::Core;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

SharedBoolean::SharedBoolean() : data ( false )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Read Object.
//
///////////////////////////////////////////////////////////////////////////////

RETURN_TYPE SharedBoolean::readObject ( vpr::ObjectReader *reader )
{
  reader->readBool ( data );
  
#if __VPR_version < 1001005
  return vpr::ReturnStatus::Succeed;
#endif
}


///////////////////////////////////////////////////////////////////////////////
//
//  Write Object.
//
///////////////////////////////////////////////////////////////////////////////

RETURN_TYPE SharedBoolean::writeObject ( vpr::ObjectWriter *writer )
{
  writer->writeBool( data );

#if __VPR_version < 1001005
  return vpr::ReturnStatus::Succeed;
#endif
}
