///////////////////////////////////////////////////////////////////////////////
//
//  Set the menu.
//
///////////////////////////////////////////////////////////////////////////////

void BaseMenu::menu ( MenuKit::Menu *menu )
{
  MENUKIT_CHECK_POINTER ( 2619032555u, menu );

  // Set the new menu.
  _menu = menu;

  // Give the menu-behavior its root menu.
  _behavior->root ( _menu.get() );
}


