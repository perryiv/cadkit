//
///////////////////////////////////////////////////////////////////////////////

ViewTabComponent::ViewTabComponent() 
{
}

///////////////////////////////////////////////////////////////////////////////
//
// Destructor
//
///////////////////////////////////////////////////////////////////////////////

ViewTabComponent::~ViewTabComponent() 
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *ViewTabComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin* > ( this );
  case Usul::Interfaces::IFoxAddTab::IID:
    return static_cast < Usul::Interfaces::IFoxAddTab* > ( this );
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
// Usul/Interfaces/IPlugin.h implementation
//
///////////////////////////////////////////////////////////////////////////////

std::string ViewTabComponent::getPluginName() const 
{
  return "View Tab";
}

	
///////////////////////////////////////////////////////////////////////////////
//
//  Add the frame tab to tab control
//
///////////////////////////////////////////////////////////////////////////////

void ViewTabComponent::foxAddTab( Usul::Interfaces::IUnknown *caller )
{
  _frameTab.initialize ( caller );
}
