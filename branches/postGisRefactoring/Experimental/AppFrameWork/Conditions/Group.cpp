///////////////////////////////////////////////////////////////////////////////
//
//  Base class for a group of conditions.
//
///////////////////////////////////////////////////////////////////////////////

#include "AppFrameWork/Conditions/Group.h"

using namespace AFW::Conditions;

USUL_IMPLEMENT_TYPE_ID ( Group );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Group::Group ( bool want ) : BaseClass ( want ),
  _kids()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Group::~Group()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Append a condition.
//
///////////////////////////////////////////////////////////////////////////////

void Group::append ( AFW::Conditions::Condition *c )
{
  Guard guard ( this->mutex() );
  if ( c )
    _kids.insert ( _kids.end(), c );
}
