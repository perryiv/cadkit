///////////////////////////////////////////////////////////////////////////////
//
//  Called when the thread finishes normally.
//
///////////////////////////////////////////////////////////////////////////////

void Job::_threadFinished()
{
  USUL_TRACE_SCOPE;

  // Declare up here.
  ScopedDone done ( *this, true );

  // See if we've been cancelled.
  if ( true == this->canceled() )
    this->cancel();

  // Call finish callback.
  this->_finished();
}


