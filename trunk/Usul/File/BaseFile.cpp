#include "Usul/File/BaseFile.h"
#include "Usul/File/Stats.h"

using namespace Usul::File;

USUL_IMPLEMENT_TYPE_ID ( BaseFile );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

BaseFile::BaseFile ( const std::string &name ) : BaseClass(),
  _name ( name )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

BaseFile::~BaseFile()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the file name.
//
///////////////////////////////////////////////////////////////////////////////

std::string BaseFile::name() const
{
  ReadLock lock ( this );
  return _name;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the size of the file.
//
///////////////////////////////////////////////////////////////////////////////

BaseFile::SizeType BaseFile::size ( const std::string &file )
{
  const SizeType size ( Usul::File::Stats<SizeType>::size ( file ) );
  return size;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the size of the file.
//
///////////////////////////////////////////////////////////////////////////////

BaseFile::SizeType BaseFile::size() const
{
  const SizeType size ( BaseFile::size ( this->name() ) );
  return size;
}
