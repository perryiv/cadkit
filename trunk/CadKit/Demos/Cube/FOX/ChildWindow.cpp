///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

ChildWindow::ChildWindow() : SgFoxChildWindow()
{
  // Don't use this constructor.
  SL_ASSERT ( 0 );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

ChildWindow::ChildWindow ( const FXString &name, MainWindow *parent ) : 
  SgFoxChildWindow ( parent->getClientArea(), name, NULL, parent->getMdiMenu(), 0, 20, 20, 400, 300 )
{
  // Build the scene.
  _root->addChild ( CadKit::Demos::buildScene ( 100, true ) );
}


