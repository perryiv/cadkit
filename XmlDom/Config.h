#ifndef _XML_POLICY_CLASSES_H_
#define _XML_POLICY_CLASSES_H_


namespace XML {
namespace Config {


///////////////////////////////////////////////////////////////////////////////
//
//  Class to trim the value-string.
//
///////////////////////////////////////////////////////////////////////////////

struct Trim
{
  template < class String > void operator () ( String &s )
  {
    // Lose leading white space.
    while ( !s.empty() && ::isspace ( s[0] ) )
      s.erase ( s.begin() );

    // Lose trailing white space.
    while ( !s.empty() && ::isspace ( s[s.size()-1] ) )
      s.erase ( s.size() - 1 );
  }
};


///////////////////////////////////////////////////////////////////////////////
//
//  Class that doesn't trim the value-string.
//
///////////////////////////////////////////////////////////////////////////////

struct NoTrim
{
  template < class String > void operator () ( String &s ){}
};


}; // namespace Config
}; // namespace XML


#endif // _XML_POLICY_CLASSES_H_
