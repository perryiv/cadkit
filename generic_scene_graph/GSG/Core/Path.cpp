///////////////////////////////////////////////////////////////////////////////
//
//  A node path.
//
///////////////////////////////////////////////////////////////////////////////

#include "GSG/Core/Precompiled.h"
#include "GSG/Core/Path.h"

using namespace GSG;

GSG_IMPLEMENT_REFERENCED ( Path );


/////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
/////////////////////////////////////////////////////////////////////////////

Path::Path() : Referenced(),
  _stack()
{

}


/////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
/////////////////////////////////////////////////////////////////////////////

Path::Path ( const Path &p ) : Referenced ( p ),
  _stack ( p._stack )
{

}


/////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
/////////////////////////////////////////////////////////////////////////////

Path::~Path()
{

}


/////////////////////////////////////////////////////////////////////////////
//
//  Push the node.
//
/////////////////////////////////////////////////////////////////////////////

void Path::push ( Node *n )
{
  Lock lock ( this );
  _stack.push ( n );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Pop the node.
//
/////////////////////////////////////////////////////////////////////////////

void Path::pop()
{
  Lock lock ( this );
  ErrorChecker ( false == _stack.empty() );
  _stack.pop();
}


/////////////////////////////////////////////////////////////////////////////
//
//  Set from the given object.
//
/////////////////////////////////////////////////////////////////////////////

void Path::setFrom ( const Path &s )
{
  Lock lock ( this );

  // Set the members.
  _stack = s._stack;

  // Call the base class's function.
  BaseClass::setFrom ( s );
}
