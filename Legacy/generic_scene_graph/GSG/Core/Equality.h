#ifndef _GENERIC_SCENE_GRAPH_CORE_TEMPLATE_EQUALITY_OPERATOR_H_
#define _GENERIC_SCENE_GRAPH_CORE_TEMPLATE_EQUALITY_OPERATOR_H_


namespace GSG {


///////////////////////////////////////////////////////////////////////////////
//
//  Equality.
//
///////////////////////////////////////////////////////////////////////////////

template < class T > inline bool operator == ( const T &a, typename T &b )
{
  return a.equal ( b );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Equality.
//
///////////////////////////////////////////////////////////////////////////////

template < class T > inline bool operator != ( const T &a, typename T &b )
{
  return !( a.equal ( b ) );
}


}; // namespace GSG


#endif // _GENERIC_SCENE_GRAPH_CORE_TEMPLATE_EQUALITY_OPERATOR_H_
