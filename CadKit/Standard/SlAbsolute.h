///////////////////////////////////////////////////////////////////////////////
//
//  SlAbsolute: Inline function(s) for taking the absolute value.
//
///////////////////////////////////////////////////////////////////////////////

#ifndef _CADKIT_STANDARD_LIBRARY_INLINE_ABSOLUTE_VALUE_H_
#define _CADKIT_STANDARD_LIBRARY_INLINE_ABSOLUTE_VALUE_H_


namespace CadKit
{
///////////////////////////////////////////////////////////////////////////////
//
//  Absolute value. Object must have operator - () defined.
//
///////////////////////////////////////////////////////////////////////////////

template<class T> inline T abs ( const T &num )
{
  return ( num < 0 ) ? -num : num;
}


}; // namespace CadKit.


///////////////////////////////////////////////////////////////////////////////
//
// For convenience and backward compatability.
//
///////////////////////////////////////////////////////////////////////////////

#define SL_ABS CadKit::abs


#endif // _CADKIT_STANDARD_LIBRARY_INLINE_ABSOLUTE_VALUE_H_
