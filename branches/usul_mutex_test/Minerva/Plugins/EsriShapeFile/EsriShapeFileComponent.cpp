#include <algorithm>

USUL_DECLARE_COMPONENT_FACTORY ( EsriShapeFileComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( EsriShapeFileComponent, EsriShapeFileComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

EsriShapeFileComponent::EsriShapeFileComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

EsriShapeFileComponent::~EsriShapeFileComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *EsriShapeFileComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin * > ( this );
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate * > ( this );
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *EsriShapeFileComponent::createDocument ( Unknown *caller )
{
  EsriShapeFileDocument::ValidRefPtr document ( new EsriShapeFileDocument );
  return document.release();
}
