#include "SgPrecompiled.h"
#include "SgQuadMesh.h"

#ifndef _CADKIT_USE_PRECOMPILED_HEADERS
# include "Standard/SlPrint.h"
# include "Standard/SlAssert.h"
#endif

using namespace CadKit;

SG_IMPLEMENT_DYNAMIC_NODE(SgQuadMesh,SgVertexSet);


/////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
/////////////////////////////////////////////////////////////////////////////

SgQuadMesh::SgQuadMesh() : SgVertexSet(),
  _numRows ( 0 ),
  _numColumns  ( 0 )
{
  SL_PRINT2 ( "SgQuadMesh::SgQuadMesh(), this = %X\n", this );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
/////////////////////////////////////////////////////////////////////////////

SgQuadMesh::~SgQuadMesh()
{
  SL_PRINT2 ( "SgQuadMesh::~SgQuadMesh(), this = %X\n", this );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Set the size.
//
/////////////////////////////////////////////////////////////////////////////

void SgQuadMesh::setSize ( const SlInt32 &numRows, const SlInt32 &numColumns )
{
  SL_ASSERT ( numRows >= 2 );
  SL_ASSERT ( numColumns >= 2 );

  _numRows = numRows;
  _numColumns = numColumns;
}
