#include "Factory.h"
#include "BinaryParser.h"
#include "TextParser.h"

using namespace Minerva::Core::postGIS;


///////////////////////////////////////////////////////////////////////////////
//
//  Initialize static data member.
//
///////////////////////////////////////////////////////////////////////////////

Factory* Factory::_instance ( 0x0 );


///////////////////////////////////////////////////////////////////////////////
//
//  Get the instance.
//
///////////////////////////////////////////////////////////////////////////////

Factory& Factory::instance()
{
  if( 0x0 == _instance )
    _instance = new Factory;

  return *_instance;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Factory::Factory()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Factory::~Factory()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Create the geometry.
//
///////////////////////////////////////////////////////////////////////////////

Geometry* Factory::createFromBinary ( const unsigned char *buffer ) const
{
  BinaryParser parser;
  return parser ( buffer );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Create the geometry.
//
///////////////////////////////////////////////////////////////////////////////

Geometry* Factory::createFromText   ( const char *buffer ) const 
{
  TextParser parser;
  return parser ( buffer );
}
