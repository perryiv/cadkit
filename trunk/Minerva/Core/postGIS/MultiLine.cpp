#include "MultiLine.h"

using namespace Minerva::Core::postGIS;

///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiLine::MultiLine() : BaseClass(),
_lines()
{
}

///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

MultiLine::~MultiLine()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a line.
//
///////////////////////////////////////////////////////////////////////////////

void MultiLine::addLine( Line* line )
{
  if( 0x0 != line )
    _lines.push_back ( line );
}


///////////////////////////////////////////////////////////////////////////////
//
//  Add a geometry.  Check to make sure it's a line.
//
///////////////////////////////////////////////////////////////////////////////

void MultiLine::addGeometry( Geometry* geometry )
{
  if( Line* line = dynamic_cast < Line* > ( geometry ) )
    this->addLine ( line );
}

