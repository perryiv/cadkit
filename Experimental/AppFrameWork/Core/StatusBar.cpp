#include "AppFrameWork/Core/StatusBar.h"
#include "AppFrameWork/Core/BaseVisitor.h"

using namespace AFW::Core;

AFW_IMPLEMENT_OBJECT ( StatusBar );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

StatusBar::StatusBar() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

StatusBar::~StatusBar()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Accept the visitor.
//
///////////////////////////////////////////////////////////////////////////////

void StatusBar::accept ( AFW::Core::BaseVisitor *v )
{
  Guard guard ( this->mutex() );
  if ( v )
    v->visit ( this );
}
