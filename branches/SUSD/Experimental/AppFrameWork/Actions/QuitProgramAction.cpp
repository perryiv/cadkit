///////////////////////////////////////////////////////////////////////////////
//
//  Command action for exiting the program.
//
///////////////////////////////////////////////////////////////////////////////

#include "AppFrameWork/Actions/QuitProgramAction.h"
#include "AppFrameWork/Core/Program.h"
#include "AppFrameWork/Core/Application.h"

using namespace AFW::Actions;

USUL_IMPLEMENT_TYPE_ID ( QuitProgramAction );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

QuitProgramAction::QuitProgramAction() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

QuitProgramAction::~QuitProgramAction()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Perform the action.
//
///////////////////////////////////////////////////////////////////////////////

void QuitProgramAction::execute ( AFW::Core::Object * )
{
  Guard guard ( this->mutex() );
  AFW_GUARD_PROGRAM;
  AFW::Core::Program::instance().app()->quit();
}
