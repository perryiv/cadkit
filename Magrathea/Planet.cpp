///////////////////////////////////////////////////////////////////////////////
//
//  Set the show lat long flag.
//
///////////////////////////////////////////////////////////////////////////////

void Planet::showLatLongGrid( bool b )
{
  if( b )
    _planet->addChild( _latLongGrid.get() );
  else
    _planet->removeChild( _latLongGrid.get() );
}


