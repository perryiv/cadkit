#ifndef _USUL_MATH_POWER_H_
#define _USUL_MATH_POWER_H_


namespace Usul {
namespace Math {


/////////////////////////////////////////////////////////////////////////////
//
//  Return the square of the value.
//
/////////////////////////////////////////////////////////////////////////////

template < class T > inline T square ( T v )
{
  return ( v * v );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Return the cube of the value.
//
/////////////////////////////////////////////////////////////////////////////

template < class T > inline T cube ( T v )
{
  return ( v * v * v );
}


} // namespace Math
} // namespace Usul


#endif // _USUL_MATH_POWER_H_
