#include "DRTSimComponent.h"
#include "DRTSimDocument.h"

#include "Usul/Components/Factory.h"

#include <algorithm>

USUL_DECLARE_COMPONENT_FACTORY ( DRTSimComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( DRTSimComponent, DRTSimComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

DRTSimComponent::DRTSimComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

DRTSimComponent::~DRTSimComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *DRTSimComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin * > ( this );
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate * > ( this );
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *DRTSimComponent::createDocument ( Unknown *caller )
{
  DRTSimDocument::ValidRefPtr document ( new DRTSimDocument );
  return document.release();
}

