#include "ModflowModel/ModflowComponent.h"
#include "ModflowModel/ModflowDocument.h"

#include "Usul/Components/Factory.h"

USUL_IMPLEMENT_TYPE_ID ( ModflowComponent );
USUL_DECLARE_COMPONENT_FACTORY ( ModflowComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( ModflowComponent, ModflowComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

ModflowComponent::ModflowComponent() : BaseClass()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

ModflowComponent::~ModflowComponent()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *ModflowComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin*>(this);
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate*>(this);
  default:
    return 0x0;
  }
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *ModflowComponent::createDocument ( Unknown *caller )
{
  Modflow::ModflowDocument::ValidRefPtr document ( new Modflow::ModflowDocument );
  return document.release();
}
