///////////////////////////////////////////////////////////////////////////////
//
//  Separator class for menus.
//
///////////////////////////////////////////////////////////////////////////////

#include "FoxTools/Menu/Separator.h"
#include "FoxTools/Menu/Group.h"
#include "FoxTools/Headers/MenuSeparator.h"

#include "Usul/Errors/Assert.h"

using namespace FoxTools;
using namespace FoxTools::Menu;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Separator::Separator ( unsigned int token ) : BaseClass ( token ),
  _separator ( 0x0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Separator::~Separator()
{
  delete _separator;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Clear the button.
//
///////////////////////////////////////////////////////////////////////////////

void Separator::clear()
{
  delete _separator;
  _separator = 0x0;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Build the button.
//
///////////////////////////////////////////////////////////////////////////////

void Separator::_build ( FX::FXComposite *parent )
{
  if ( !_separator )
    _separator = new FX::FXMenuSeparator ( parent );
}
