///////////////////////////////////////////////////////////////////////////////
//
//  A pool of 2D vectors.
//
///////////////////////////////////////////////////////////////////////////////

#include "GSG/Core/Precompiled.h"
#include "GSG/Core/Vec2Pool.h"

using namespace GSG;

GSG_IMPLEMENT_REFERENCED ( Vec2Pool );


/////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
/////////////////////////////////////////////////////////////////////////////

Vec2Pool::Vec2Pool() : Referenced(), 
  _values()
{
  // Empty.
}


/////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
/////////////////////////////////////////////////////////////////////////////

Vec2Pool::Vec2Pool ( const Vec2Pool &p ) : Referenced ( p ), 
  _values ( p._values )
{
  // Empty.
}


/////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
/////////////////////////////////////////////////////////////////////////////

Vec2Pool::~Vec2Pool()
{
  // Empty.
}


///////////////////////////////////////////////////////////////////////////////
//
//  Access to the values.
//
///////////////////////////////////////////////////////////////////////////////

const Vec2Pool::value_type &Vec2Pool::value ( Vec2Pool::size_type i ) const
{
  BoundsChecker ( _values.size(), i );
  return _values[i];
}


///////////////////////////////////////////////////////////////////////////////
//
//  Access to the values.
//
///////////////////////////////////////////////////////////////////////////////

void Vec2Pool::value ( Vec2Pool::size_type i, const Vec2Pool::value_type &v )
{
  BoundsChecker ( _values.size(), i );
  _values[i] = v;
}


/////////////////////////////////////////////////////////////////////////////
//
//  Set from the given object.
//
/////////////////////////////////////////////////////////////////////////////

void Vec2Pool::setFrom ( const Vec2Pool &p )
{
  Lock lock ( this );

  // Set the members.
  _values = p._values;

  // Call the base class's function.
  BaseClass::setFrom ( p );
}
