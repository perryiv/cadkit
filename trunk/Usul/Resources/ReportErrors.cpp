///////////////////////////////////////////////////////////////////////////////
//
//  Get and set pointer to report errors.
//
///////////////////////////////////////////////////////////////////////////////

#include "Usul/Resources/ReportErrors.h"


///////////////////////////////////////////////////////////////////////////////
//
//  Pointer to report errors.
//
///////////////////////////////////////////////////////////////////////////////

namespace Usul
{
  namespace Resources
  {
    namespace Detail
    {
      Usul::Interfaces::IReportErrors *_report ( 0x0 );
    }
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get pointer to report errors. It may be null.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IReportErrors *Usul::Resources::reportErrors()
{
  return Detail::_report;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set pointer to report errors.
//
///////////////////////////////////////////////////////////////////////////////

void Usul::Resources::reportErrors ( Usul::Interfaces::IReportErrors *report )
{
  Detail::_report = report;
} 
