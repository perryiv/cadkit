///////////////////////////////////////////////////////////////////////////////
//
//  Frame class to arrange child windows.
//
///////////////////////////////////////////////////////////////////////////////

#include "AppFrameWork/Core/Frame.h"
#include "AppFrameWork/Core/BaseVisitor.h"

using namespace AFW::Core;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Frame::Frame ( ChildLayout l ) : BaseClass(),
  _layout ( l )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Frame::~Frame()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the child-layout.
//
///////////////////////////////////////////////////////////////////////////////

void Frame::layout ( ChildLayout l )
{
  _layout = l;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the child-layout.
//
///////////////////////////////////////////////////////////////////////////////

Frame::ChildLayout Frame::layout()
{
  return _layout;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Accept the visitor.
//
///////////////////////////////////////////////////////////////////////////////

void Frame::accept ( AFW::Core::BaseVisitor *v )
{
  if ( v )
    v->visit ( this );
}
