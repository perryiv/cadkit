#include "Minerva/Layers/Kml/Style.h"

#include "XmlTree/Node.h"

using namespace Minerva::Layers::Kml;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Style::Style() : BaseClass(),
  _iconstyle ( 0x0 ),
  _linestyle ( 0x0 ),
	_polystyle ( 0x0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Style::Style ( const XmlTree::Node& node ) : BaseClass ( node ),
  _iconstyle ( 0x0 ),
  _linestyle ( 0x0 ),
	_polystyle ( 0x0 )
{
	typedef XmlTree::Node::Children Children;
  
  Children children ( node.children() );
  for ( Children::iterator iter = children.begin(); iter != children.end(); ++iter )
  {
    XmlTree::Node::RefPtr node ( *iter );
    std::string name ( node->name() );
    
    if ( "PolyStyle" == name )
    {
			_polystyle = new PolyStyle ( *node );
    }
    else if ( "LineStyle" == name )
    {
      _linestyle = new LineStyle ( *node );
    }
    else if ( "IconStyle" == name )
    {
      _iconstyle = new IconStyle ( *node );
    }
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Style::~Style()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the polystyle.
//
///////////////////////////////////////////////////////////////////////////////

void Style::polystyle ( PolyStyle * polystyle )
{
	_polystyle = polystyle;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the polystyle.
//
///////////////////////////////////////////////////////////////////////////////

PolyStyle* Style::polystyle() const
{
	return _polystyle.get();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the polystyle.
//
///////////////////////////////////////////////////////////////////////////////

void Style::linestyle ( LineStyle * linestyle )
{
	_linestyle = linestyle;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the polystyle.
//
///////////////////////////////////////////////////////////////////////////////

LineStyle* Style::linestyle() const
{
	return _linestyle.get();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the iconstyle.
//
///////////////////////////////////////////////////////////////////////////////

void Style::iconstyle ( IconStyle * style )
{
  _iconstyle = style;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the iconstyle.
//
///////////////////////////////////////////////////////////////////////////////

IconStyle* Style::iconstyle() const
{
  return _iconstyle.get();
}
