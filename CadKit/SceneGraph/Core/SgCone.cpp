#include "SgPrecompiled.h"
#include "SgCone.h"

#ifndef _CADKIT_USE_PRECOMPILED_HEADERS
# include "Standard/SlPrint.h"
#endif

using namespace CadKit;

SG_IMPLEMENT_DYNAMIC_NODE(SgCone,SgPrimitive);


/////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
/////////////////////////////////////////////////////////////////////////////

SgCone::SgCone() : SgPrimitive(),
  baseCenter ( 0.0f, 0.0f, 0.0f ),
  radius ( 1.0f ),
  height ( 1.0f )
{
  SL_PRINT2 ( "SgCone::SgCone(), this = %X\n", this );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
/////////////////////////////////////////////////////////////////////////////

SgCone::SgCone ( const SlVec3f &bc, const SlFloat32 &r, const SlFloat32 &h ) : SgPrimitive(),
  baseCenter ( bc ),
  radius ( r ),
  height ( h )
{
  SL_PRINT2 ( "SgCone::SgCone(), this = %X\n", this );
}


/////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
/////////////////////////////////////////////////////////////////////////////

SgCone::~SgCone()
{
  SL_PRINT2 ( "SgCone::SgCone(), this = %X\n", this );
}
