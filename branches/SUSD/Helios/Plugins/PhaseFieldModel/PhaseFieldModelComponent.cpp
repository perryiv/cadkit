#include "PhaseFieldModelComponent.h"
#include "PhaseFieldDocument.h"

USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( PhaseFieldModelComponent , PhaseFieldModelComponent::BaseClass );

///////////////////////////////////////////////////////////////////////////////
//
// Constructor
//
///////////////////////////////////////////////////////////////////////////////

PhaseFieldModelComponent::PhaseFieldModelComponent() 
{
}

///////////////////////////////////////////////////////////////////////////////
//
// Destructor
//
///////////////////////////////////////////////////////////////////////////////

PhaseFieldModelComponent::~PhaseFieldModelComponent() 
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *PhaseFieldModelComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin* > ( this );
  case Usul::Interfaces::IDocumentCreate::IID:
    return static_cast < Usul::Interfaces::IDocumentCreate*>(this);
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
// Usul/Interfaces/IPlugin.h implementation
//
///////////////////////////////////////////////////////////////////////////////

std::string PhaseFieldModelComponent::getPluginName() const 
{
  return "Phase Field Model";
}


/////////////////////////////////////////////////////////////////////////////
//
//  Create document.
//
/////////////////////////////////////////////////////////////////////////////

Usul::Documents::Document *PhaseFieldModelComponent::createDocument ( Unknown *caller )
{
  PhaseFieldDocument::ValidRefPtr document ( new PhaseFieldDocument );
  return document.release();
}


	
