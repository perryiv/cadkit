///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

QuadTree::QuadTree() : BaseClass(),
  _root ( 0x0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

QuadTree::~QuadTree()
{
  Usul::Pointers::unreference ( _root );
  _root = 0x0;
}
