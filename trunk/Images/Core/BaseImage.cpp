///////////////////////////////////////////////////////////////////////////////
//
//  Base class for all image implementations.
//
///////////////////////////////////////////////////////////////////////////////

#include "Images/Core/BaseImage.h"

using namespace Images;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

BaseImage::BaseImage() : BaseClass(),
  _width    ( 0 ),
  _height   ( 0 ),
  _channels ( 0 ),
  _alpha    ( false )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Copy constructor.
//
///////////////////////////////////////////////////////////////////////////////

BaseImage::BaseImage ( const BaseImage &b ) : BaseClass ( b ),
  _width    ( b._width    ),
  _height   ( b._height   ),
  _channels ( b._channels ),
  _alpha    ( b._alpha    )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

BaseImage::~BaseImage()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Resize the image.
//
///////////////////////////////////////////////////////////////////////////////

void BaseImage::resize ( unsigned int w, unsigned int h, unsigned int c )
{
  _width    = w;
  _height   = h;
  _channels = c;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the flag that says if last channel should be interpreted as alpha.
//
///////////////////////////////////////////////////////////////////////////////

void BaseImage::alpha ( bool a )
{
  _alpha = a;
}
